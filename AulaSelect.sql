--OPÇÃO PELO APELIDO DA COLUNA NOME
SELECT NOMEPRODUTO AS 'NOME',PRECOPRODUTO AS 'PREÇO' FROM TBPRODUTO ORDER BY NOME

--ORDEM ASCENDENTE ASC É OPCIONAL
SELECT NOMEPRODUTO AS 'NOME',PRECOPRODUTO AS 'PREÇO' FROM TBPRODUTO ORDER BY PRECOPRODUTO ASC
SELECT NOMEPRODUTO AS 'NOME',PRECOPRODUTO AS 'PREÇO' FROM TBPRODUTO ORDER BY PRECOPRODUTO

--ORDEM DECRESCENTE
SELECT NOMEPRODUTO AS 'NOME',PRECOPRODUTO AS 'PREÇO' FROM TBPRODUTO ORDER BY PRECOPRODUTO DESC

--ESTOQUE
SELECT NOMEPRODUTO,QUANTIAESTOQUE,QUANTIDADEMIN FROM TBPRODUTO

--CÁLCULO PARA COMPRA
SELECT NOMEPRODUTO,QUANTIAESTOQUE,QUANTIDADEMIN,QUANTIAESTOQUE-QUANTIDADEMIN AS 'QTDE' FROM
TBPRODUTO

--ODER BY
SELECT NOMEPRODUTO,QUANTIAESTOQUE,QUANTIDADEMIN,QUANTIAESTOQUE-QUANTIDADEMIN AS 'QTDE' FROM
TBPRODUTO
ORDER BY QTDE

--ORDER BY NÍVEL 2
SELECT NOMEPRODUTO,QUANTIAESTOQUE,QUANTIDADEMIN,QUANTIAESTOQUE-QUANTIDADEMIN AS 'QTDE' FROM
TBPRODUTO
ORDER BY QTDE ASC,NOMEPRODUTO ASC

--SEM USAR O APELIDO
SELECT NOMEPRODUTO,QUANTIAESTOQUE,QUANTIDADEMIN,QUANTIAESTOQUE-QUANTIDADEMIN FROM TBPRODUTO
