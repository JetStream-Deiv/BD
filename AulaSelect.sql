--REGRA DE PESQUISA USANDO O WHERE
SELECT NOMEPRODUTO,QUANTIAESTOQUE,QUANTIDADEMIN,QUANTIAESTOQUE-QUANTIDADEMIN FROM TBPRODUTO
WHERE QUANTIAESTOQUE-QUANTIDADEMIN <= 5
ORDER BY QUANTIAESTOQUE-QUANTIDADEMIN ASC,NOMEPRODUTO ASC
